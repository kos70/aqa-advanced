const number = 12;

console.log(`\nТаблиця множення для числа ${number}:`);
for (let i = 1; i <= 10; i++) {
  console.log(`${number} * ${i} = ${number * i}`);
}

console.log(`\nТаблиця множення для числа ${number}:`);
let j = 1;
while (j <= 10) {
  console.log(`${number} * ${j} = ${number * j}`);
  j++;
}